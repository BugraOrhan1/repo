def get_brands():
    seen = []
    for v in VEHICLES:
        if v[0] not in seen:
            seen.append(v[0])
    return sorted(seen)


def get_models(brand: str):
    seen = []
    for v in VEHICLES:
        if v[0] == brand and v[1] not in seen:
            seen.append(v[1])
    return sorted(seen)


def get_generations(brand: str, model: str):
    seen = []
    for v in VEHICLES:
        if v[0] == brand and v[1] == model and v[2] not in seen:
            seen.append(v[2])
    return seen


def get_engines(brand: str, model: str, generation: str):
    matching = [v for v in VEHICLES if v[0] == brand and v[1] == model and v[2] == generation]
    name_counts = {}
    for v in matching:
        name_counts[v[3]] = name_counts.get(v[3], 0) + 1
    items = []
    for v in matching:
        # If multiple engines share same name, suffix with HP for clarity
        name = f"{v[3]} {v[4]}hp" if name_counts[v[3]] > 1 else v[3]
        items.append({
            'name': name,
            'hp': v[4],
            'kw': v[5],
            'fuel': {'P': 'Petrol', 'D': 'Diesel', 'H': 'Hybrid', 'E': 'Electric'}.get(v[6], 'Other'),
            'ecus': v[7].split('|') if v[7] else [],
        })
    return items


def get_ecus(brand: str, model: str, generation: str, engine: str):
    matching = [v for v in VEHICLES if v[0] == brand and v[1] == model and v[2] == generation]
    name_counts = {}
    for v in matching:
        name_counts[v[3]] = name_counts.get(v[3], 0) + 1
    for v in matching:
        name = f"{v[3]} {v[4]}hp" if name_counts[v[3]] > 1 else v[3]
        # Match either exact name OR raw engine name with hp suffix
        if name == engine or v[3] == engine:
            return v[7].split('|') if v[7] else []
    return []
